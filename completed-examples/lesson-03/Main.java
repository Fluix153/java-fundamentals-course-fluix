class Main {
  public static void main(String[] args) {
    int edad = 15;

    // 1. Sentencia if-else
    if (edad >= 18) {
      System.out.println("Eres mayor de edad.");
    } else {
      System.out.println("Eres menor de edad.");
    }

    // 2. Bucle for
    System.out.println("Voy a contar hasta 3:");
    for (int i = 1; i <= 3; i++) {
      System.out.println(i);
    }
  }
}